def add_one(number):
    """Return the given number plus one.

    Example
    -------
    >>> add_one(2)
    3
    """
    return number + 1

def add_two(number):
    """Return the given number plus two.

    Example
    -------
    >>> add_two(2)
    4
    """
    return number + 2

def add_three(number):
    """Return the given number plus three.

    Example
    -------
    >>> add_three(2)
    5
    """
    return number + 3
